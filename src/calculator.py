def str_to_int(str_1, str_2):
    val_1 = int(str_1)
    val_2 = int(str_2)
    return val_1, val_2


def int_to_str(int_1):
    str_1 = str(int_1)
    return str_1

def add(num_1, num_2):
    str_to_int(num_1, num_2)
    ans = num_1 + num_2
    return ans

