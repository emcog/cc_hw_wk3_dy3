
def add(num_1, num_2):
    val_1 = int(num_1)
    val_2 = int(num_2)
    ans = val_1 + val_2
    return str(ans)

def subtract(num_1, num_2):
    val_1 = int(num_1)
    val_2 = int(num_2)
    ans = val_1 - val_2
    return str(ans)

def multiply(num_1, num_2):
    val_1 = int(num_1)
    val_2 = int(num_2)
    ans = val_1 * val_2
    return str(ans)

def divide(num_1, num_2):
    val_1 = int(num_1)
    val_2 = int(num_2)
    ans = val_1 // val_2
    return str(ans)